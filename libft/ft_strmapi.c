#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*ans;

	ans = (char *)malloc(sizeof (char) * (ft_strlen(s) + 1));
	if (!ans)
		return (0);
	i = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		ans[i] = f(i, s[i]);
		i++;
	}
	ans[i] = '\0';
	return (ans);
}
